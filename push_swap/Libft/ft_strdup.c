#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;
	size_t	s1_len;
	size_t	i;

	s1_len = ft_strlen(s1);
	result = (char *)malloc(s1_len + 1);
	if (result == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
